 // Display sum of n Natural Numbers
public class ForLoop2 {
    public static void main (String args[])
    {
        int sum=0, n=5;
        for(int i = 0; i <= n; i++) {
            sum =sum + i;
            
        }
        System.out.println("The sum of N natural Numbers are :" + sum);

       

    }
    
}
